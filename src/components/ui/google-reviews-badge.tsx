"use client";

import { motion } from "framer-motion";
import { Star } from "lucide-react";

interface GoogleReviewsBadgeProps {
  rating?: number;
  reviewCount?: number;
  compact?: boolean;
  className?: string;
}

export function GoogleReviewsBadge({
  rating = 5.0,
  reviewCount = 47,
  compact = false,
  className = "",
}: GoogleReviewsBadgeProps) {
  const stars = Array.from({ length: 5 }, (_, i) => {
    const filled = i < Math.floor(rating);
    const partial = !filled && i < rating;
    return { filled, partial };
  });

  if (compact) {
    return (
      <div className={`flex items-center gap-2 ${className}`}>
        <div className="flex items-center gap-1">
          <svg className="w-5 h-5" viewBox="0 0 24 24">
            <path
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
              fill="#4285F4"
            />
            <path
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              fill="#34A853"
            />
            <path
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              fill="#FBBC05"
            />
            <path
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              fill="#EA4335"
            />
          </svg>
          <div className="flex">
            {stars.map((star, i) => (
              <Star
                key={i}
                className={`w-4 h-4 ${
                  star.filled
                    ? "fill-amber-400 text-amber-400"
                    : star.partial
                    ? "fill-amber-400/50 text-amber-400"
                    : "fill-slate-600 text-slate-600"
                }`}
              />
            ))}
          </div>
          <span className="text-sm font-semibold text-white">{rating}</span>
        </div>
        <span className="text-sm text-slate-400">({reviewCount} reviews)</span>
      </div>
    );
  }

  return (
    <motion.div
      initial={{ opacity: 0, y: 20 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ delay: 0.3 }}
      className={`inline-flex items-center gap-3 px-4 py-2.5 rounded-full bg-white/10 backdrop-blur-sm border border-white/20 ${className}`}
    >
      {/* Google Logo */}
      <svg className="w-6 h-6" viewBox="0 0 24 24">
        <path
          d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
          fill="#4285F4"
        />
        <path
          d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
          fill="#34A853"
        />
        <path
          d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
          fill="#FBBC05"
        />
        <path
          d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
          fill="#EA4335"
        />
      </svg>

      <div className="flex flex-col">
        <div className="flex items-center gap-1.5">
          {/* Stars */}
          <div className="flex">
            {stars.map((star, i) => (
              <Star
                key={i}
                className={`w-4 h-4 ${
                  star.filled
                    ? "fill-amber-400 text-amber-400"
                    : star.partial
                    ? "fill-amber-400/50 text-amber-400"
                    : "fill-slate-600 text-slate-600"
                }`}
              />
            ))}
          </div>
          <span className="text-sm font-bold text-white">{rating}</span>
        </div>
        <span className="text-xs text-slate-300">
          {reviewCount} Google Reviews
        </span>
      </div>
    </motion.div>
  );
}

// Star rating display component
export function StarRating({ rating = 5, size = "md" }: { rating?: number; size?: "sm" | "md" | "lg" }) {
  const sizeClasses = {
    sm: "w-3 h-3",
    md: "w-4 h-4",
    lg: "w-5 h-5",
  };

  return (
    <div className="flex">
      {Array.from({ length: 5 }, (_, i) => (
        <Star
          key={i}
          className={`${sizeClasses[size]} ${
            i < rating
              ? "fill-amber-400 text-amber-400"
              : "fill-slate-600 text-slate-600"
          }`}
        />
      ))}
    </div>
  );
}
